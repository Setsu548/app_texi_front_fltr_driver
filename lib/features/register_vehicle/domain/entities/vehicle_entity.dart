class VehicleEntity {
  final String brand;
  final String model;
  final String vin;
  final String? insurancePolicy;
  final int year;
  final String licensePlate;
  final String color;
  final String tittleDeed;

  VehicleEntity({
    required this.brand,
    required this.model,
    required this.vin,
    required this.insurancePolicy,
    required this.year,
    required this.licensePlate,
    required this.color,
    required this.tittleDeed,
  });
}
